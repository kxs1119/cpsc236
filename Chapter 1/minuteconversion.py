
seconds = int(input("Enter the amount of seconds: ")) # data of seconds
minutes = seconds // 60 # converting seconds to minutes
hours = minutes // 60   # converting minutes to hours 

print("Here is the amount of seconds inputted: " + str(seconds)) # display seconds
print("Here is the amount of minutes inputted: " + str(minutes)) # display minutes 
print("Here is the amount of hours inputted: " + str(hours))     # display hours


